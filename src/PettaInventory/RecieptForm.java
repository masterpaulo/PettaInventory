    public void printReceipt(InventoryItem purchase, double []trans){
        int qty = purchase.getItemQuantity();
        String name = purchase.getItemName();
        double total = purchase.getItemPrice()*qty;
        
        String items[] = {String.format("%4d  %-30s %11.2f", qty, name, total)};
        
        String post[] = new String[4];
        post[0]= "--------------------------------------------------";
        post[1]= String.format(" Total Amount Due: %29.2f ", trans[0]);
        post[2]= String.format(" Cash Tendered:    %29.2f ", trans[1]);
        post[3]= String.format(" Change:           %29.2f ", trans[2]);
        
        
        String []receipt = connect(header , items);
        receipt = connect(receipt, post);
        receipt = connect(receipt, footer);
        list.setListData(receipt);
    }
    
    public void printReceipt(InventoryItem []purchases, double []trans){
        String items[] = new String[purchases.length];
        
        for(int i=0;i<purchases.length;i++){
            int qty = purchases[i].getItemQuantity();
            String name = purchases[i].getItemName();
            double total = purchases[i].getItemPrice()*qty;
        
            items[i] = String.format("%4d  %-30s %11.2f", qty, name, total);
        }
        
        String post[] = new String[4];
        post[0]= "--------------------------------------------------";
        post[1]= String.format(" Total Amount Due: %29.2f ", trans[0]);
        post[2]= String.format(" Cash Tendered:    %29.2f ", trans[1]);
        post[3]= String.format(" Change:           %29.2f ", trans[2]);
        
        
        String []receipt = connect(header , items);
        receipt = connect(receipt, post);
        receipt = connect(receipt, footer);
        list.setListData(receipt);
    }
    public static String[] connect(String[] str1, String[] str2){
        String str[] = new String[str1.length+str2.length];
        int i;
        for(i=0;i<str1.length;i++)
            str[i] = str1[i];
        for(int j=0;j<str2.length;j++,i++)
            str[i]=str2[j];
        return str;
    }
    
